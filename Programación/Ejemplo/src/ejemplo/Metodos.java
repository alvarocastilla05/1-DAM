public class Metodos {

	/**
	 * Método que une dos Strings
	 * @param str1 Primer valor a concatenar
	 * @param str2 Segundo valor a concatenar
	 * @return dos String concatenados
	 */
	
	public String concatenar(String str1, String str2) {
		
		return str1+str2;
	}
	
	/**
	 * Método que multiplica dos números decimales
	 * @since 1.0
	 * @see "Libro de Matemáticas"
	 * @param a Primer valor deciamal
	 * @param b Segundo valor decimal
	 * @return el producto de dos números decimales
	 */
	
	public double multiplicar(double a, double b) {
		
		return a*b;
	}
}
